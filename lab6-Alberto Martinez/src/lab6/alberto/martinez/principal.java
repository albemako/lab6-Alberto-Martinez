    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        if (jl_seresvivos.getSelectedIndex()>=0){
            DefaultListModel modelo=(DefaultListModel) jl_seresvivos.getModel();
            U.datos.remove(jl_seresvivos.getSelectedIndex());
            
            
            modelo.removeAllElements();
            for (int i = 0; i <U.datos.size() ; i++) {
                modelo.addElement(U.datos.get(i));
            }

            jl_seresvivos.setModel(modelo);
            
            DefaultListModel modelo2=(DefaultListModel) jl_registro.getModel();
        modelo2.removeAllElements();
        for (int i = 0; i <U.datos.size() ; i++) {
            modelo2.addElement(U.datos.get(i));
        }

        jl_registro.setModel(modelo2);
        }else{
            JOptionPane.showMessageDialog(this, "No selecciono ningun Ser vivo!");
        }
    }//GEN-LAST:event_jButton10ActionPerformed

