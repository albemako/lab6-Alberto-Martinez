    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        if (jl_seresvivos.getSelectedIndex()>=0){
            String raza;
            int ki;
            int max;
            String planeta;
            
            raza=JOptionPane.showInputDialog("Nueva Raza");
            ki=Integer.parseInt(JOptionPane.showInputDialog("Nuevo KI"));
            max=Integer.parseInt(JOptionPane.showInputDialog("Nueva edad maxima (años)"));
            planeta=JOptionPane.showInputDialog("Nuevo planeta");
            
            U.datos.get(jl_seresvivos.getSelectedIndex()).setRaza(raza);
            U.datos.get(jl_seresvivos.getSelectedIndex()).setKi(ki);
            U.datos.get(jl_seresvivos.getSelectedIndex()).setMax(max);
            U.datos.get(jl_seresvivos.getSelectedIndex()).setPlaneta(planeta);
            
            
            DefaultListModel modelo=(DefaultListModel) jl_seresvivos.getModel();
            
            
            
            modelo.removeAllElements();
            for (int i = 0; i <U.datos.size() ; i++) {
                modelo.addElement(U.datos.get(i));
            }

            jl_seresvivos.setModel(modelo);
        }else{
            JOptionPane.showMessageDialog(this, "No selecciono ningun Ser vivo!");
        }
    }//GEN-LAST:event_jButton9ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        JFileChooser fc= new JFileChooser();
        //FileFilter filtro=new FileNameExtensionFilter(".txt");
        //fc.setFileFilter(filtro);
        File archivo;
        int op=fc.showOpenDialog(this);
        if (op==JFileChooser.APPROVE_OPTION) {
            archivo=fc.getSelectedFile();
            nombreuniverso=archivo.getName();
            U=new Universo("./"+nombreuniverso+".txt", archivo.getName());
            U.setArchivo(archivo);
            U.cargarArchivo();
            
            DefaultListModel modelo=(DefaultListModel) jl_registro.getModel();
            modelo.removeAllElements();
            for (int i = 0; i <U.datos.size() ; i++) {
                modelo.addElement(U.datos.get(i));
            }
            DefaultListModel modelo2=(DefaultListModel) jl_seresvivos.getModel();
            
            modelo2.removeAllElements();
            for (int i = 0; i <U.datos.size() ; i++) {
                modelo2.addElement(U.datos.get(i));
            }

            jl_seresvivos.setModel(modelo2);

            jl_registro.setModel(modelo);

        }
    }//GEN-LAST:event_jButton3ActionPerformed

